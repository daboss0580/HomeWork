package Class9HW;

public class HW080423N8 {
    public static void main(String[] args) {
        //Create an array to store char values and then print those in reverse order`
        char [] val={'a','b','c','d','f'};
        for (int a=val.length; a<=0; a--){
            System.out.println(val[a]);
        }
    }
}
